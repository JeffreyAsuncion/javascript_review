{
    // while
    // do while
    // for 

    // initialization
    // condition
    // update

    let i = 0; // initialization
    while(i < 10){  // condition
        // code
        console.log(i);
        i++; // update
    }

    // do-while loop executes at least once
    let j = 0; // initialization
    do{
        // code 
        console.log(j);
        j++; // update
    } while(j < 10); // condition

    for(let k = 0; k < 10; k++){
        console.log(k);
    }
}